# type casting = convert the data type of value to another data type.

x = 1    # int
y = 2.0  # float
z = "3"  # str

# reassign if you need to permanently change (immutable)
x = int(x)
y = int(y)
z = int(z)

x = float(x)
y = float(y)
z = float(z)

x = str(x)
y = str(y)
z = str(z)

print(x)
print(y)
print(z * 3)
